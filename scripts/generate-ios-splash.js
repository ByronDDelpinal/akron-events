#!/usr/bin/env node
/**
 * generate-ios-splash.js
 *
 * iOS is the only platform that needs pre-rendered launch images: a
 * standalone PWA with no <link rel="apple-touch-startup-image"> flashes a
 * blank screen on launch. There's no single scalable splash — iOS matches
 * an exact image per device via a media query (device-width/height + dpr +
 * orientation), so we render one PNG per device class here.
 *
 * Each image is the brand cream (--bg-page) with the app icon centered,
 * so the splash dissolves seamlessly into the cream first paint (the boot
 * shell in index.html). Portrait only — phones launch portrait, and a
 * missing match just falls back to today's blank, never something broken.
 *
 * Reproducible by design (no mystery binaries in the repo): re-run after
 * changing the icon or adding a device.
 *
 *   node scripts/generate-ios-splash.js          # write public/splash/*
 *   node scripts/generate-ios-splash.js --html   # also print the <link> tags
 *
 * Requires ImageMagick (`convert`).
 */
import { execFileSync } from 'node:child_process'
import { mkdirSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const PUBLIC = join(ROOT, 'public')
const OUT_DIR = join(PUBLIC, 'splash')
const SOURCE_ICON = join(PUBLIC, 'pwa-1024x1024.png')
const BG = '#FCFAF4' // matches --bg-page (default theme) in src/styles/globals.css

/**
 * Device classes by CSS width/height and devicePixelRatio. Physical pixels
 * (what the PNG must be) = css * dpr. Rows that resolve to the same physical
 * size still need distinct media queries, so they're listed individually.
 */
const DEVICES = [
  { w: 375,  h: 667,  dpr: 2 }, // iPhone SE (2/3), 8, 7, 6s
  { w: 414,  h: 736,  dpr: 3 }, // iPhone 8 Plus
  { w: 375,  h: 812,  dpr: 3 }, // iPhone X, XS, 11 Pro
  { w: 414,  h: 896,  dpr: 2 }, // iPhone XR, 11
  { w: 414,  h: 896,  dpr: 3 }, // iPhone XS Max, 11 Pro Max
  { w: 390,  h: 844,  dpr: 3 }, // iPhone 12, 13, 14
  { w: 393,  h: 852,  dpr: 3 }, // iPhone 14 Pro, 15, 15 Pro, 16
  { w: 428,  h: 926,  dpr: 3 }, // iPhone 12/13 Pro Max, 14 Plus
  { w: 430,  h: 932,  dpr: 3 }, // iPhone 14 Pro Max, 15 Plus, 15 Pro Max
  { w: 768,  h: 1024, dpr: 2 }, // iPad 9.7/10.2, mini
  { w: 820,  h: 1180, dpr: 2 }, // iPad 10.9 (Air 4/5, iPad 10)
  { w: 834,  h: 1112, dpr: 2 }, // iPad Air 3, Pro 10.5
  { w: 834,  h: 1194, dpr: 2 }, // iPad Pro 11
  { w: 1024, h: 1366, dpr: 2 }, // iPad Pro 12.9
]

function fileFor(d) {
  return `apple-splash-${d.w * d.dpr}-${d.h * d.dpr}.png`
}

function linkTag(d) {
  const media =
    `(device-width: ${d.w}px) and (device-height: ${d.h}px) ` +
    `and (-webkit-device-pixel-ratio: ${d.dpr}) and (orientation: portrait)`
  return `    <link rel="apple-touch-startup-image" media="${media}" href="/splash/${fileFor(d)}" />`
}

function render(d) {
  const pw = d.w * d.dpr
  const ph = d.h * d.dpr
  const icon = Math.round(Math.min(pw, ph) * 0.3) // ~30% of the short edge
  const out = join(OUT_DIR, fileFor(d))
  execFileSync('convert', [
    '-size', `${pw}x${ph}`, `xc:${BG}`,
    '(', SOURCE_ICON, '-resize', `${icon}x${icon}`, ')',
    '-gravity', 'center', '-composite',
    out,
  ])
  return out
}

function main() {
  mkdirSync(OUT_DIR, { recursive: true })
  for (const d of DEVICES) {
    render(d)
    console.error(`✓ ${fileFor(d)}`)
  }
  console.error(`\nGenerated ${DEVICES.length} splash images in public/splash/`)
  if (process.argv.includes('--html')) {
    console.log('\n<!-- iOS launch images (generated by scripts/generate-ios-splash.js) -->')
    console.log(DEVICES.map(linkTag).join('\n'))
  }
}

main()
